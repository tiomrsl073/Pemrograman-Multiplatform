//struktur perulangan menggunakan for
void main(List<String> args) {
  int i = 0;
  while (i < 100) {
    print("Baris $i ");
    i = i + 3;
  } 
  //JIKA KONDISI NYA BENAR MAKA AKAN DILAKUKAN PERULANGAN NAMUN JIKA SALAH
  //MAKA AKAN DIHENTIKAN ATAU KELUAR DARI KODE PROGRAM
print("========================================");
for (int i = 0; i < 5; i++) {
print('Baris $i');
}
List<int> list = [10, 20, 30, 40, 50];
for (int elemen in list) {
print(elemen);
}
}
