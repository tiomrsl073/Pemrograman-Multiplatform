//operator relasional
void main(List<String> args) {
int a = 9, b = 10;
print('$a == $b: ${a == b}');
print('$a != $b: ${a != b}');
//operator yang mengggunakan hubungan relasi.
//operator ini menghasilkan tipe data boolean
print('$a > $b: ${a > b}');
print('$a >= $b: ${a >= b}');
print('$a < $b: ${a < b}');
print('$a <= $b: ${a <= b}');
}